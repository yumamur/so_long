#include "so_long.h"

int	ft_strcmp(t_c_char *s1, t_c_char *s2)
{
	size_t	i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
		i++;
	return ((t_uchar) s1[i] - (t_uchar) s2[i]);
}
